package src.basics_02;

public class Lab015 {
    public static void main(String[] args) {

        char c = '\n'; // new line
        char c1 = '\t'; // add tab
        char c2 = '\b'; // backslash , delete 1 char
        char c3 = '\r'; //delete 1 string
        System.out.println("Vivek"+ c + "Kumar");
        System.out.println("Vivek"+ c1 + "Kumar");
        System.out.println("Vivek"+ c2 + "Kumar");
        System.out.println("Vivek"+ c3 + "Kumar");
        System.out.println("Vivek\tKumar");
        System.out.println("Vivek\'s");
    }
}
